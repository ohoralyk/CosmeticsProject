package our.project.cosmetic;

public class IngredientDTO {
	
	private int ingno;
	private String ingname;
	private String lisklevel;
	private String datagrade;
	private String useful;
	private String harmful;
	private String caution;
	private String function;
	
	public int getIngno() {
		return ingno;
	}
	public void setIngno(int ingno) {
		this.ingno = ingno;
	}
	public String getIngname() {
		return ingname;
	}
	public void setIngname(String ingname) {
		this.ingname = ingname;
	}
	public String getLisklevel() {
		return lisklevel;
	}
	public void setLisklevel(String lisklevel) {
		this.lisklevel = lisklevel;
	}
	public String getDatagrade() {
		return datagrade;
	}
	public void setDatagrade(String datagrade) {
		this.datagrade = datagrade;
	}
	public String getUseful() {
		return useful;
	}
	public void setUseful(String useful) {
		this.useful = useful;
	}
	public String getHarmful() {
		return harmful;
	}
	public void setHarmful(String harmful) {
		this.harmful = harmful;
	}
	public String getCaution() {
		return caution;
	}
	public void setCaution(String caution) {
		this.caution = caution;
	}
	public String getFunction() {
		return function;
	}
	public void setFunction(String function) {
		this.function = function;
	}
	
}
